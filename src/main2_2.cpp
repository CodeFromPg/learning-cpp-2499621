#include <iostream>

int main()
{
  // Erste Variable
  int meineErsteVariable = 4;

  // Zweite Variable
  int meineZweiteVariable = 2;

  // Ergebnis
  int meinErgebnis = meineErsteVariable + meineZweiteVariable;

  // Ausgabe
  std::cout << "Erste Variable " << meineErsteVariable << std::endl;
  std::cout << "Zweite Variable " << meineZweiteVariable << std::endl;
  std::cout << "Ergebnis " << meinErgebnis << std::endl;

  return 0;
}